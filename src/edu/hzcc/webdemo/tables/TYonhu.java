package edu.hzcc.webdemo.tables;

public class TYonhu {
	private Long id;
	private int yudingnian;
	private String name;
	private String dingdan;
	private String hangban;
	private String zhuangtai;
	private int zuowei;
	private String piaojia;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public int getYudingnian() {
		return yudingnian;
	}
	public void setYudingnian(int yudingnian) {
		this.yudingnian = yudingnian;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDingdan() {
		return dingdan;
	}
	public void setDingdan(String dingdan) {
		this.dingdan = dingdan;
	}
	public String getHangban() {
		return hangban;
	}
	public void setHangban(String hangban) {
		this.hangban = hangban;
	}
	public String getZhuangtai() {
		return zhuangtai;
	}
	public void setZhuangtai(String zhuangtai) {
		this.zhuangtai = zhuangtai;
	}
	public int getZuowei() {
		return zuowei;
	}
	public void setZuowei(int zuowei) {
		this.zuowei = zuowei;
	}
	public String getPiaojia() {
		return piaojia;
	}
	public void setPiaojia(String piaojia) {
		this.piaojia = piaojia;
	}
	
}
